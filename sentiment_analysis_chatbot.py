from textblob import TextBlob
import nltk

# NLTK ka data download karein
nltk.download('punkt')

# Sentiment Analysis Function
def analyze_sentiment(text):
    analysis = TextBlob(text)
    polarity = analysis.sentiment.polarity

    if polarity > 0:
        return "Positive", polarity
    elif polarity < 0:
        return "Negative", polarity
    else:
        return "Neutral", polarity

# Chatbot Function
def chatbot_response(user_input):
    sentiment, polarity = analyze_sentiment(user_input)

    if sentiment == "Positive":
        return f"Wow! You sound happy. ðŸ˜Š (Sentiment: {sentiment}, Polarity: {polarity})"
    elif sentiment == "Negative":
        return f"Oh no! You seem upset. ðŸ˜” (Sentiment: {sentiment}, Polarity: {polarity})"
    else:
        return f"Hmm, interesting. ðŸ¤” (Sentiment: {sentiment}, Polarity: {polarity})"

